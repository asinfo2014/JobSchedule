package com.bean;

public class FlowTaskBean {
	private long taskid;
	private String taskname;
	private long tarmacid;
	private String execmd;
	private long flowid;
	
	public long getTaskid() {
		return taskid;
	}
	public void setTaskid(long taskid) {
		this.taskid = taskid;
	}
	
	public String getTaskname() {
		return taskname;
	}
	public void setTaskname(String taskname) {
		this.taskname = taskname;
	}
	public long getTarmacid() {
		return tarmacid;
	}
	public void setTarmacid(long tarmacid) {
		this.tarmacid = tarmacid;
	}
	public String getExecmd() {
		return execmd;
	}
	public void setExecmd(String execmd) {
		this.execmd = execmd;
	}
	public long getFlowid() {
		return flowid;
	}
	public void setFlowid(long flowid) {
		this.flowid = flowid;
	}
	
	
}
